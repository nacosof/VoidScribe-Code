export type I18nKey = keyof (typeof messages)["ru"];

export function t(
  lang: UiLanguage,
  key: I18nKey,
  arg?: number | string
): string {
  const value = messages[lang][key];
  if (typeof value === "function") {
    return value(arg as never);
  }
  return value;
}

export function themeLabel(lang: UiLanguage, theme: UiTheme): string {
  switch (theme) {
    case "voidscribe":
      return t(lang, "themeVoidscribe");
    case "slate":
      return t(lang, "themeSlate");
    case "ocean":
      return t(lang, "themeOcean");
  }
}
