export type I18nKey = keyof (typeof messages)["ru"];
export function t(lang: UiLanguage | undefined, key: I18nKey, arg?: number | string): string {
    const value = messages[lang ?? "en"][key];
    if (typeof value === "function") {
        return (value as (arg: number | string) => string)(arg as number | string);
    }
    return value;
}
export function themeLabel(lang: UiLanguage, theme: UiTheme): string {
    switch (theme) {
        case "voidscribe":
            return t(lang, "themeVoidscribe");
        case "slate":
            return t(lang, "themeSlate");
        case "ocean":
            return t(lang, "themeOcean");
    }
}
